package multi.android.material_design_pro2.exam2;

public class CardViewItem {
    int res;
    String intro;

    public CardViewItem(int res, String intro) {
        this.res = res;
        this.intro = intro;
    }

    public int getRes() {
        return res;
    }

    public String getIntro() {
        return intro;
    }
}
